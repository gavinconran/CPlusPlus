#include <iostream>
using namespace std;

int main() {
    int i = 7;
    unsigned int j = i;      // from int to unsigned int
    short int k = i;         // from int to short
    double x = i;            // from int to double
    
    cout << i << " " << j << " " << k << " " << x << endl;
    return 0;
}


